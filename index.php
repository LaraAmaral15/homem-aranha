		<h1>Homem Aranha</h1>
		<p>
			
			O Homem-Aranha (Spider-Man no original) o alter-ego de Peter <br>
			Parker,é um personagem fictício, um super-herói que aparece nas <br>
			revistas em quadrinhos americanas publicadas pela Marvel Comics, <br>
			existindo no seu universo partilhado. <br><br>
			O Homem-Aranha foi criado pelo editor/escritor Stan Lee e pelo  <br>
			escritor/artista Steve Ditko, e a sua primeira aparição foi no livro de  <br>
			antologia Amazing Fantasy #15 (Agosto de 1962), durante a Era de <br>
			Prata da banda desenhada. Lee e Ditko conceberam o personagem  <br>
			como um órfão, que foi educado e criado pela sua Tia May e o seu Tio <br>
			Ben em Nova Iorque, e, enquanto adolescente, tem de lidar com as  <br>
			lutas diárias normais da sua idade, em adição àquelas que tem como  <br>
			combatente do crime mascarado. <br><br>
			Para combater os seus inimigos, os criadores deram-lhe super força e  <br>
			agilidade, a habilidade de conseguir aderir na maior parte das  <br>
			superfícies, a possibilidade de disparar teias de aranha através de <br>
			mecanismos montados nos pulsos (inventados por ele próprio, a que ele <br>
			chama "lança-teia) e consegue reagir <br>
			precognitivamente ao perigo com o seu "sentido-aranha" <br>
			Os seus poderes foram adquiridos após ter sido mordido por uma  <br>
			aranha radioativa. <br>
